/**
 * EMAIL AND SMS CONFIGURATION SCRIPT
 * Interactive setup for email and SMS services
 */

/* eslint-disable no-console */

import readline from 'readline';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout,
});

const question = (query) => {
  return new Promise((resolve) => rl.question(query, resolve));
};

const config = {
  email: {},
  sms: {},
};

console.log('='.repeat(60));
console.log('EMAIL AND SMS CONFIGURATION WIZARD');
console.log('='.repeat(60));
console.log('\nThis wizard will help you configure email and SMS services.');
console.log('Press Ctrl+C at any time to cancel.\n');

async function configureEmail() {
  console.log('\n📧 EMAIL CONFIGURATION');
  console.log('-'.repeat(60));

  const provider = await question(
    '\nSelect email provider:\n1. SendGrid\n2. AWS SES\n3. SMTP (Gmail, etc.)\nChoice (1-3): '
  );

  switch (provider.trim()) {
    case '1':
      config.email.EMAIL_PROVIDER = 'sendgrid';
      config.email.SENDGRID_API_KEY = await question(
        'Enter SendGrid API Key: '
      );
      break;

    case '2':
      config.email.EMAIL_PROVIDER = 'ses';
      config.email.AWS_SES_ACCESS_KEY_ID = await question(
        'Enter AWS SES Access Key ID: '
      );
      config.email.AWS_SES_SECRET_ACCESS_KEY = await question(
        'Enter AWS SES Secret Access Key: '
      );
      config.email.AWS_SES_REGION = await question(
        'Enter AWS SES Region (default: us-east-1): '
      );
      if (!config.email.AWS_SES_REGION) {
        config.email.AWS_SES_REGION = 'us-east-1';
      }
      break;

    case '3':
      config.email.EMAIL_PROVIDER = 'smtp';
      config.email.SMTP_HOST = await question('Enter SMTP Host: ');
      config.email.SMTP_PORT = await question('Enter SMTP Port (default: 587): ');
      if (!config.email.SMTP_PORT) {
        config.email.SMTP_PORT = '587';
      }
      config.email.SMTP_USER = await question('Enter SMTP Username/Email: ');
      config.email.SMTP_PASS = await question('Enter SMTP Password: ');
      const secure = await question('Use SSL/TLS? (y/n, default: n): ');
      config.email.SMTP_SECURE = secure.toLowerCase() === 'y' ? 'true' : 'false';
      break;

    default:
      console.log('Invalid choice. Skipping email configuration.');
      return;
  }

  config.email.EMAIL_FROM = await question(
    'Enter "From" email address (e.g., noreply@yourdomain.com): '
  );
  config.email.EMAIL_FROM_NAME = await question(
    'Enter "From" name (e.g., Your Company Name): '
  );

  console.log('\n✅ Email configuration complete!');
}

async function configureSMS() {
  console.log('\n📱 SMS CONFIGURATION');
  console.log('-'.repeat(60));

  const provider = await question(
    '\nSelect SMS provider:\n1. Twilio\n2. AWS SNS\n3. Nexmo/Vonage\n4. Skip SMS configuration\nChoice (1-4): '
  );

  switch (provider.trim()) {
    case '1':
      config.sms.SMS_PROVIDER = 'twilio';
      config.sms.TWILIO_ACCOUNT_SID = await question(
        'Enter Twilio Account SID: '
      );
      config.sms.TWILIO_AUTH_TOKEN = await question(
        'Enter Twilio Auth Token: '
      );
      config.sms.TWILIO_PHONE_NUMBER = await question(
        'Enter Twilio Phone Number (e.g., +1234567890): '
      );
      break;

    case '2':
      config.sms.SMS_PROVIDER = 'aws-sns';
      config.sms.AWS_SNS_ACCESS_KEY_ID = await question(
        'Enter AWS SNS Access Key ID: '
      );
      config.sms.AWS_SNS_SECRET_ACCESS_KEY = await question(
        'Enter AWS SNS Secret Access Key: '
      );
      config.sms.AWS_SNS_REGION = await question(
        'Enter AWS SNS Region (default: us-east-1): '
      );
      if (!config.sms.AWS_SNS_REGION) {
        config.sms.AWS_SNS_REGION = 'us-east-1';
      }
      break;

    case '3':
      config.sms.SMS_PROVIDER = 'nexmo';
      config.sms.NEXMO_API_KEY = await question('Enter Nexmo API Key: ');
      config.sms.NEXMO_API_SECRET = await question('Enter Nexmo API Secret: ');
      config.sms.NEXMO_FROM_NUMBER = await question(
        'Enter Nexmo From Number: '
      );
      break;

    case '4':
      console.log('Skipping SMS configuration.');
      return;

    default:
      console.log('Invalid choice. Skipping SMS configuration.');
      return;
  }

  console.log('\n✅ SMS configuration complete!');
}

function generateEnvContent() {
  let content = '# EMAIL AND SMS CONFIGURATION\n';
  content += '# Generated by configure-email-sms.js\n';
  content += `# Date: ${new Date().toISOString()}\n\n`;

  content += '# ===== EMAIL CONFIGURATION =====\n';
  for (const [key, value] of Object.entries(config.email)) {
    content += `${key}=${value}\n`;
  }

  content += '\n# ===== SMS CONFIGURATION =====\n';
  for (const [key, value] of Object.entries(config.sms)) {
    content += `${key}=${value}\n`;
  }

  return content;
}

async function saveConfiguration() {
  console.log('\n💾 SAVING CONFIGURATION');
  console.log('-'.repeat(60));

  const envPath = path.join(__dirname, '..', '.env');
  const envExamplePath = path.join(__dirname, '..', '.env.example');

  // Check if .env exists
  let existingContent = '';
  if (fs.existsSync(envPath)) {
    const overwrite = await question(
      '\n.env file already exists. Append to it? (y/n): '
    );
    if (overwrite.toLowerCase() === 'y') {
      existingContent = fs.readFileSync(envPath, 'utf8');
      existingContent += '\n\n';
    } else {
      // Create backup
      const backupPath = `${envPath}.backup.${Date.now()}`;
      fs.copyFileSync(envPath, backupPath);
      console.log(`\n📋 Backup created: ${backupPath}`);
    }
  }

  // Generate new content
  const newContent = generateEnvContent();

  // Write to .env
  fs.writeFileSync(envPath, existingContent + newContent, 'utf8');
  console.log(`\n✅ Configuration saved to: ${envPath}`);

  // Update .env.example if it exists
  if (fs.existsSync(envExamplePath)) {
    const updateExample = await question(
      '\nUpdate .env.example with template? (y/n): '
    );
    if (updateExample.toLowerCase() === 'y') {
      const exampleContent = generateEnvContent().replace(
        /=.+$/gm,
        '=your_value_here'
      );
      fs.writeFileSync(envExamplePath, exampleContent, 'utf8');
      console.log(`✅ .env.example updated`);
    }
  }
}

async function testConfiguration() {
  console.log('\n🧪 TEST CONFIGURATION');
  console.log('-'.repeat(60));

  const runTests = await question(
    '\nWould you like to test the configuration? (y/n): '
  );

  if (runTests.toLowerCase() === 'y') {
    console.log('\nRunning tests...');
    console.log(
      '\nTo test your configuration, run:\n  node test_email_sms_config.js\n'
    );
  }
}

function displaySummary() {
  console.log('\n📊 CONFIGURATION SUMMARY');
  console.log('='.repeat(60));

  console.log('\n📧 Email Configuration:');
  if (Object.keys(config.email).length > 0) {
    console.log(`  Provider: ${config.email.EMAIL_PROVIDER || 'Not configured'}`);
    console.log(`  From: ${config.email.EMAIL_FROM || 'Not configured'}`);
    console.log(`  From Name: ${config.email.EMAIL_FROM_NAME || 'Not configured'}`);
  } else {
    console.log('  Not configured');
  }

  console.log('\n📱 SMS Configuration:');
  if (Object.keys(config.sms).length > 0) {
    console.log(`  Provider: ${config.sms.SMS_PROVIDER || 'Not configured'}`);
  } else {
    console.log('  Not configured');
  }

  console.log('\n' + '='.repeat(60));
}

async function main() {
  try {
    await configureEmail();
    await configureSMS();
    displaySummary();
    await saveConfiguration();
    await testConfiguration();

    console.log('\n✨ Configuration complete!');
    console.log('\nNext steps:');
    console.log('1. Review your .env file');
    console.log('2. Run: node test_email_sms_config.js');
    console.log('3. Start your application\n');
  } catch (error) {
    console.error('\n❌ Error:', error.message);
  } finally {
    rl.close();
  }
}

main();
